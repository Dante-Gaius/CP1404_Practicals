import random
print(random.randint(5, 20))  # line 1
# The smallest number in line 1 will be 5 and the largest number will be 19
print(random.randrange(3, 10, 2))  # line 2
# The smallest number in line 2 will be 3 and the largest number will be 9
print(random.uniform(2.5, 5.5))  # line 3
# The smallest number in line 3 will be 2.5 and the largest number will be 5.5
